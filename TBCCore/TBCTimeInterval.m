#import "TBCTimeInterval.h"

const NSTimeInterval kTBCTimeIntervalOneMinute = 60;
const NSTimeInterval kTBCTimeIntervalOneHour = 60 * kTBCTimeIntervalOneMinute;
const NSTimeInterval kTBCTimeIntervalOneDay = 24 * kTBCTimeIntervalOneHour;
const NSTimeInterval kTBCTimeIntervalOneWeek = 7 * kTBCTimeIntervalOneDay;
const NSTimeInterval kTBCTimeIntervalOneYear = 365 * kTBCTimeIntervalOneDay;
